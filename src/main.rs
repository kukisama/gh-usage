#[derive(Debug, Parser)]
#[command(name = "gh-usage")]
#[command(
    about = "Scan local GitHub Copilot / VS Code Chat usage records and export credit usage as CSV",
    version
)]
struct Cli {
    /// VS Code workspaceStorage root. Defaults to the standard VS Code user-data path for the current OS.
    #[arg(long)]
    vscode_workspace_storage: Option<PathBuf>,

    /// Copilot CLI root. Reserved for optional probing; defaults to ~/.copilot.
    #[arg(long)]
    copilot_cli_root: Option<PathBuf>,

    /// Include generic Copilot CLI log probing. Slower and currently best-effort.
    #[arg(long)]
    include_cli_logs: bool,

    /// Only scan files modified within N days.
    #[arg(long)]
    since_days: Option<u64>,

    /// Maximum files to scan, useful while experimenting.
    #[arg(long)]
    max_files: Option<usize>,

    /// Output CSV path. Use '-' for stdout. Defaults to copilot-usage-<hostname>.csv beside the binary's working directory.
    #[arg(long, short)]
    output: Option<PathBuf>,

    /// Output format. CSV is the default for spreadsheet inspection.
    #[arg(long, value_enum, default_value_t = OutputFormat::Csv)]
    format: OutputFormat,

    /// Override the machine hostname embedded into records. Defaults to the detected OS hostname.
    #[arg(long)]
    hostname: Option<String>,

    /// Explicit path for the HTML summary report. Implies HTML output. Defaults to copilot-usage-<hostname>.html beside the CSV.
    #[arg(long)]
    html: Option<PathBuf>,

    /// Skip writing the HTML summary report. HTML is generated by default whenever a file output is produced.
    #[arg(long)]
    no_html: bool,

    /// Print a short summary to stderr after writing output.
    #[arg(long)]
    summary: bool,

    /// Do not write a UTF-8 BOM for file output. By default files include BOM for Windows Excel compatibility.
    #[arg(long)]
    no_bom: bool,

    /// Merge mode. Read every `copilot-usage-*.csv` in DIR (or the current directory when DIR is omitted)
    /// and render a combined `copilot-usage-merged.html` next to those CSVs. Skips the local VS Code scan.
    #[arg(long, value_name = "DIR", num_args = 0..=1, default_missing_value = ".")]
    merge: Option<PathBuf>,
}

#[derive(Debug, Clone, Copy, ValueEnum)]
enum OutputFormat {
    Csv,
    Json,
}

#[derive(Debug, Clone, Default)]
struct ContextFields {
    session_id: Option<String>,
    request_id: Option<String>,
    response_id: Option<String>,
    model_id: Option<String>,
    agent_id: Option<String>,
    timestamp_ms: Option<i64>,
}

#[derive(Debug, Clone, Serialize, Deserialize)]
struct UsageRecord {
    #[serde(default)]
    source: String,
    #[serde(default)]
    hostname: String,
    #[serde(default)]
    timestamp_ms: Option<i64>,
    #[serde(default)]
    local_time_hint: Option<String>,
    #[serde(default)]
    chat_title: Option<String>,
    #[serde(default)]
    model: String,
    #[serde(default)]
    model_id: Option<String>,
    #[serde(default)]
    credits: f64,
    #[serde(default)]
    details: String,
    #[serde(default)]
    session_id: Option<String>,
    #[serde(default)]
    request_id: Option<String>,
    #[serde(default)]
    response_id: Option<String>,
    #[serde(default)]
    agent_id: Option<String>,
    #[serde(default)]
    file: String,
    #[serde(default)]
    line: usize,
}

#[derive(Debug, Default)]
struct ScanStats {
    scanned_files: usize,
    scanned_lines: usize,
    json_candidate_lines: usize,
    parse_errors: usize,
}

#[derive(Debug, Default)]
struct FileScanResult {
    records: Vec<UsageRecord>,
    scanned_lines: usize,
    json_candidate_lines: usize,
    parse_errors: usize,
}

#[derive(Debug, Default)]
struct LineExtraction {
    response_metadata: HashMap<String, ContextFields>,
    records: Vec<RawUsageRecord>,
}

#[derive(Debug)]
struct RawUsageRecord {
    context: ContextFields,
    model: String,
    credits: f64,
    details: String,
}

fn main() -> Result<()> {
    let total_started = Instant::now();
    let no_args = env::args_os().len() == 1;
    let double_clicked = no_args && launched_from_explorer();
    let cli = Cli::parse();

    if let Some(dir) = cli.merge.clone() {
        return run_merge(&dir, double_clicked);
    }

    let hostname = cli
        .hostname
        .clone()
        .map(|name| sanitize_hostname(&name))
        .unwrap_or_else(detect_hostname);

    let output_path = cli
        .output
        .clone()
        .unwrap_or_else(|| default_output_path(&hostname, cli.format));

    let html_path = if cli.no_html {
        None
    } else if let Some(path) = cli.html.clone() {
        Some(path)
    } else if output_path.as_os_str() == "-" {
        None
    } else {
        Some(default_html_path(&output_path, &hostname))
    };

    let vscode_root = cli
        .vscode_workspace_storage
        .or_else(default_vscode_workspace_storage)
        .context(
            "Could not determine VS Code workspaceStorage path; pass --vscode-workspace-storage",
        )?;

    let copilot_cli_root = cli.copilot_cli_root.or_else(default_copilot_cli_root);
    let cutoff = cli.since_days.map(cutoff_system_time);

    let discover_started = Instant::now();
    let mut files = collect_vscode_chat_session_files(&vscode_root, cutoff.as_ref())?;

    if cli.include_cli_logs {
        if let Some(root) = copilot_cli_root.as_ref() {
            files.extend(collect_cli_probe_files(root, cutoff.as_ref())?);
        }
    }

    files.sort();
    files.dedup();

    if let Some(max_files) = cli.max_files {
        files.truncate(max_files);
    }
    let discover_ms = discover_started.elapsed().as_millis();

    let scanned_files = files.len();
    let scan_started = Instant::now();
    let file_results: Vec<FileScanResult> = files.par_iter().map(|path| scan_file(path)).collect();
    let scan_ms = scan_started.elapsed().as_millis();

    let reduce_started = Instant::now();
    let mut stats = ScanStats {
        scanned_files,
        ..ScanStats::default()
    };
    let mut records = Vec::new();

    for result in file_results {
        stats.scanned_lines += result.scanned_lines;
        stats.json_candidate_lines += result.json_candidate_lines;
        stats.parse_errors += result.parse_errors;
        records.extend(result.records);
    }

    records.sort_by(|a, b| {
        a.timestamp_ms
            .cmp(&b.timestamp_ms)
            .then_with(|| a.file.cmp(&b.file))
            .then_with(|| a.line.cmp(&b.line))
            .then_with(|| a.response_id.cmp(&b.response_id))
    });
    records.dedup_by(|a, b| {
        a.file == b.file
            && a.line == b.line
            && a.response_id == b.response_id
            && a.details == b.details
    });
    for record in &mut records {
        record.hostname = hostname.clone();
    }
    let reduce_ms = reduce_started.elapsed().as_millis();

    let write_started = Instant::now();
    match cli.format {
        OutputFormat::Csv => write_csv(&output_path, &records, !cli.no_bom)?,
        OutputFormat::Json => write_json(&output_path, &records, !cli.no_bom)?,
    }
    if let Some(html_path) = html_path.as_ref() {
        write_html(html_path, &records, &hostname)?;
    }
    let write_ms = write_started.elapsed().as_millis();
    let total_ms = total_started.elapsed().as_millis();

    if no_args || cli.summary {
        let report = build_summary_report(
            &output_path,
            html_path.as_deref(),
            &records,
            &stats,
            discover_ms,
            scan_ms,
            reduce_ms,
            write_ms,
            total_ms,
        );
        if output_path.as_os_str() == "-" {
            eprint!("{report}");
        } else {
            print!("{report}");
        }
    }

    if double_clicked {
        show_double_click_exit_notice(&output_path, html_path.as_deref());
    }

    Ok(())
}

fn show_double_click_exit_notice(output: &Path, html: Option<&Path>) {
    println!();
    println!(
        "CSV saved to {}",
        output.display()
    );
    println!(
        "  Every parsed Copilot usage record is in there - import it into Excel,"
    );
    println!(
        "  Power BI, or pipe it through your own scripts for deeper analysis."
    );
    if let Some(html) = html {
        println!();
        println!("HTML report saved to {}", html.display());
        println!(
            "  A self-contained, offline-friendly page with charts, host/model"
        );
        println!(
            "  filters, and a paginated record table. Safe to email or archive."
        );
        println!();
        println!(">> Press any key to open the HTML report in your default browser.");
        println!("   (Or close this window to skip - both files are already on disk.)");
        wait_for_any_key();
        open_path(html);
    } else {
        println!();
        println!("HTML report skipped (--no-html). The CSV above is your full dataset.");
        println!();
        println!(">> Press any key to close this window. The CSV is already saved.");
        wait_for_any_key();
    }
}

#[cfg(windows)]
fn open_path(path: &Path) {
    // `explorer.exe <path>` resolves files via the default shell handler
    // (HTML → default browser), without blocking us on the launched process.
    let _ = std::process::Command::new("explorer.exe").arg(path).spawn();
}

#[cfg(not(windows))]
fn open_path(_path: &Path) {}

#[cfg(windows)]
fn launched_from_explorer() -> bool {
    windows_parent_process_name()
        .as_deref()
        .is_some_and(|name| name.eq_ignore_ascii_case("explorer.exe"))
}

#[cfg(not(windows))]
fn launched_from_explorer() -> bool {
    false
}

#[cfg(windows)]
fn wait_for_any_key() {
    use windows_sys::Win32::Foundation::INVALID_HANDLE_VALUE;
    use windows_sys::Win32::System::Console::{
        GetConsoleMode, GetStdHandle, ReadConsoleW, SetConsoleMode, ENABLE_ECHO_INPUT,
        ENABLE_LINE_INPUT, STD_INPUT_HANDLE,
    };

    unsafe {
        let handle = GetStdHandle(STD_INPUT_HANDLE);
        if handle == INVALID_HANDLE_VALUE {
            return;
        }

        let mut original_mode = 0;
        if GetConsoleMode(handle, &mut original_mode) == 0 {
            let mut input = String::new();
            let _ = std::io::stdin().read_line(&mut input);
            return;
        }

        let single_key_mode = original_mode & !(ENABLE_LINE_INPUT | ENABLE_ECHO_INPUT);
        let _ = SetConsoleMode(handle, single_key_mode);

        let mut buffer = [0u16; 1];
        let mut chars_read = 0;
        let _ = ReadConsoleW(
            handle,
            buffer.as_mut_ptr().cast(),
            buffer.len() as u32,
            &mut chars_read,
            std::ptr::null_mut(),
        );

        let _ = SetConsoleMode(handle, original_mode);
    }
}

#[cfg(not(windows))]
fn wait_for_any_key() {}

#[cfg(windows)]
fn windows_parent_process_name() -> Option<String> {
    use windows_sys::Win32::Foundation::{CloseHandle, INVALID_HANDLE_VALUE};
    use windows_sys::Win32::System::Diagnostics::ToolHelp::{
        CreateToolhelp32Snapshot, Process32FirstW, Process32NextW, PROCESSENTRY32W,
        TH32CS_SNAPPROCESS,
    };

    unsafe {
        let snapshot = CreateToolhelp32Snapshot(TH32CS_SNAPPROCESS, 0);
        if snapshot == INVALID_HANDLE_VALUE {
            return None;
        }

        let mut entry = std::mem::zeroed::<PROCESSENTRY32W>();
        entry.dwSize = std::mem::size_of::<PROCESSENTRY32W>() as u32;

        let current_pid = std::process::id();
        let mut parent_pid = None;

        if Process32FirstW(snapshot, &mut entry) != 0 {
            loop {
                if entry.th32ProcessID == current_pid {
                    parent_pid = Some(entry.th32ParentProcessID);
                    break;
                }

                if Process32NextW(snapshot, &mut entry) == 0 {
                    break;
                }
            }
        }

        let Some(parent_pid) = parent_pid else {
            let _ = CloseHandle(snapshot);
            return None;
        };

        if Process32FirstW(snapshot, &mut entry) != 0 {
            loop {
                if entry.th32ProcessID == parent_pid {
                    let name = wide_process_name_to_string(&entry.szExeFile);
                    let _ = CloseHandle(snapshot);
                    return name;
                }

                if Process32NextW(snapshot, &mut entry) == 0 {
                    break;
                }
            }
        }

        let _ = CloseHandle(snapshot);
        None
    }
}

#[cfg(windows)]
fn wide_process_name_to_string(buffer: &[u16]) -> Option<String> {
    let len = buffer.iter().position(|ch| *ch == 0).unwrap_or(buffer.len());
    if len == 0 {
        None
    } else {
        Some(String::from_utf16_lossy(&buffer[..len]))
    }
}

/// Merge mode: read every `copilot-usage-*.csv` in `dir`, combine + dedupe,
/// render a single `copilot-usage-merged.html` next to those CSVs.
fn run_merge(dir: &Path, double_clicked: bool) -> Result<()> {
    if !dir.is_dir() {
        anyhow::bail!(
            "merge target is not a directory: {}",
            dir.display()
        );
    }

    let mut csv_files: Vec<PathBuf> = Vec::new();
    for entry in fs::read_dir(dir)
        .with_context(|| format!("read_dir failed for {}", dir.display()))?
    {
        let entry = entry?;
        let path = entry.path();
        if !path.is_file() {
            continue;
        }
        let name = match path.file_name().and_then(|s| s.to_str()) {
            Some(n) => n,
            None => continue,
        };
        let lower = name.to_ascii_lowercase();
        if !lower.starts_with("copilot-usage-") || !lower.ends_with(".csv") {
            continue;
        }
        // Skip our own merged outputs so re-running is idempotent.
        if lower == "copilot-usage-merged.csv" {
            continue;
        }
        csv_files.push(path);
    }
    csv_files.sort();

    if csv_files.is_empty() {
        anyhow::bail!(
            "no copilot-usage-*.csv files found in {}",
            dir.display()
        );
    }

    let mut records: Vec<UsageRecord> = Vec::new();
    let mut per_file: Vec<(PathBuf, usize)> = Vec::new();
    let mut row_errors: usize = 0;
    for path in &csv_files {
        let before = records.len();
        let mut reader = csv::ReaderBuilder::new()
            .flexible(true)
            .from_path(path)
            .with_context(|| format!("open csv {}", path.display()))?;
        for row in reader.deserialize::<UsageRecord>() {
            match row {
                Ok(record) => records.push(record),
                Err(_) => row_errors += 1,
            }
        }
        per_file.push((path.clone(), records.len() - before));
    }

    // Backfill hostname for legacy CSVs (pre-hostname column) using the file stem.
    for (path, _) in &per_file {
        let stem = path
            .file_stem()
            .and_then(|s| s.to_str())
            .unwrap_or_default();
        if let Some(host_from_name) = stem.strip_prefix("copilot-usage-") {
            for record in records.iter_mut().filter(|r| r.hostname.is_empty()) {
                record.hostname = host_from_name.to_owned();
            }
        }
    }

    records.sort_by(|a, b| {
        a.hostname
            .cmp(&b.hostname)
            .then_with(|| a.timestamp_ms.cmp(&b.timestamp_ms))
            .then_with(|| a.file.cmp(&b.file))
            .then_with(|| a.line.cmp(&b.line))
            .then_with(|| a.response_id.cmp(&b.response_id))
    });
    records.dedup_by(|a, b| {
        a.hostname == b.hostname
            && a.file == b.file
            && a.line == b.line
            && a.response_id == b.response_id
            && a.details == b.details
    });

    let hosts: std::collections::BTreeSet<String> =
        records.iter().map(|r| r.hostname.clone()).collect();
    let primary_host = hosts
        .iter()
        .next()
        .cloned()
        .unwrap_or_else(|| "merged".to_owned());

    let html_path = dir.join("copilot-usage-merged.html");
    write_html(&html_path, &records, &primary_host)?;

    // Compact merge summary, mirroring the look of the regular scan report.
    const INNER: usize = 54;
    let row = |content: String| -> String {
        let len = content.chars().count();
        let padded = if len < INNER {
            format!("{content}{}", " ".repeat(INNER - len))
        } else {
            content.chars().take(INNER).collect()
        };
        format!("| {padded} |\n")
    };
    let header = |title: &str| -> String {
        let prefix = format!("- {title} ");
        let pad = (INNER + 2).saturating_sub(prefix.chars().count());
        format!("+{prefix}{}+\n", "-".repeat(pad))
    };
    let plain_border = || -> String { format!("+{}+\n", "-".repeat(INNER + 2)) };

    let total_credits = normalize_zero(records.iter().map(|r| r.credits).sum());
    let host_list: Vec<&str> = hosts.iter().map(String::as_str).collect();

    let mut out = String::new();
    out.push_str(&header("GitHub Copilot Usage - Merge"));
    out.push_str(&row(format!(
        "{:<14}{:>12}  {:<14}{:>12}",
        "csv files",
        csv_files.len(),
        "row errors",
        row_errors
    )));
    out.push_str(&row(format!(
        "{:<14}{:>12}  {:<14}{:>12}",
        "records",
        records.len(),
        "hosts",
        hosts.len()
    )));
    out.push_str(&row(format!(
        "{:<14}{:>12}  {:<14}{:>12}",
        "total credits",
        format!("{:.1}", total_credits),
        "target dir",
        clip(&dir.display().to_string(), 12)
    )));

    out.push_str(&header("Sources"));
    for (path, count) in &per_file {
        let file_name = path
            .file_name()
            .and_then(|s| s.to_str())
            .unwrap_or("<unknown>");
        out.push_str(&row(format!(
            "{:<40}{:>14}",
            clip(file_name, 40),
            format!("{count} rows")
        )));
    }

    out.push_str(&header("Hosts"));
    out.push_str(&row(clip(&host_list.join(", "), INNER)));

    out.push_str(&header("Output"));
    out.push_str(&row(format!(
        "html  {}",
        clip(&html_path.display().to_string(), INNER - 6)
    )));
    out.push_str(&plain_border());
    print!("{out}");

    if double_clicked {
        show_merge_exit_notice(&html_path, &host_list);
    }
    Ok(())
}

