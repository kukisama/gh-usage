fn default_html_path(output_path: &Path, hostname: &str) -> PathBuf {
    // Place the HTML next to the primary output file, replacing the extension.
    let mut candidate = output_path.to_path_buf();
    if candidate.set_extension("html") {
        return candidate;
    }
    PathBuf::from(format!("copilot-usage-{hostname}.html"))
}

const REPORT_TEMPLATE: &str = include_str!("report-template.html");
const REPORT_DATA_PLACEHOLDER: &str = "__GH_USAGE_DATA__";

fn write_html(path: &Path, records: &[UsageRecord], primary_host: &str) -> Result<()> {
    let payload = serde_json::json!({
        "generatedAt": Local::now().format("%Y-%m-%d %H:%M:%S").to_string(),
        "primaryHost": primary_host,
        "records": records,
    });

    let mut data = serde_json::to_string(&payload)?;
    // Avoid prematurely closing the inline <script> block. JSON has no literal "</",
    // so any "</" inside string values is escaped to "<\/" which is still valid JSON.
    data = data.replace("</", "<\\/");

    let html = REPORT_TEMPLATE.replace(REPORT_DATA_PLACEHOLDER, &data);

    let mut file = File::create(path)
        .with_context(|| format!("failed to create HTML output {}", path.display()))?;
    file.write_all(html.as_bytes())?;
    Ok(())
}

